/* 设想有个机器人坐在一个网格的左上角，网格 r 行 c 列。
机器人只能向下或向右移动，但不能走到一些被禁止的网格（有障碍物）。设计一种算法，寻找机器人从左上角移动到右下角的路径。
网格中的障碍物和空位置分别用 1 和 0 来表示。
返回一条可行的路径，路径由经过的网格的行号和列号组成。左上角为 0 行 0 列。如果没有可行的路径，返回空数组。
示例1：
输入：{{0,0,0},{0,1,0},{0,0,0}}
输出：{{0,0},{0,1},{0,2},{1,2},{2,2}}
解释：
输入中标粗的位置即为输出表示的路径，即
0 行 0 列（左上角） -> 0 行 1 列 -> 0 行 2 列 -> 1 行 2 列 -> 2 行 2 列（右下角）
说明：r和 c 的值均不超过 100。 */

class Solution {
public:
	vector<vector<int>> pathWithObstacles(vector<vector<int>> &obstacleGrid) {
		if(obstacleGrid[0][0] || obstacleGrid.back().back()) return {};
		m = obstacleGrid.size(), n = obstacleGrid[0].size();
		find = false;
		vector<vector<int>> path;
		path.reserve(m + n);
		dfs(0, 0, obstacleGrid, path);
		if(find) {
			return path;
		}
		return {};
	}

private:
	bool find;
	int m, n;
	void dfs(int i, int j, vector<vector<int>> &obstacleGrid, vector<vector<int>> &path) {
		if(find) return;
		path.push_back({ i, j });
		obstacleGrid[i][j] = 1; // 避免重复访问
		if(i == m - 1 && j == n - 1) {
			find = true;
			return;
		}
		if(i < m - 1 && !obstacleGrid[i + 1][j]) {
			dfs(i + 1, j, obstacleGrid, path);
		}
		if(find) return;
		if(j < n - 1 && !obstacleGrid[i][j + 1]) {
			dfs(i, j + 1, obstacleGrid, path);
		}
		if(find) return;
		path.pop_back();
	}
}; // 需要通过修改obstacleGrid的值，防止重复访问，否则会超时

